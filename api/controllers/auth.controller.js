import User from '../models/user.model.js';
import bcryptjs from 'bcryptjs';
import { errorHandler } from '../utils/error.js';
import jwt from 'jsonwebtoken';

export const signup = async (req, res, next) => {
  const {firstName,lastname,phoneNumber, username, email, password } = req.body;
  console.log(req.body);
  const hashedPassword = bcryptjs.hashSync(password, 10);
  const newUser = new User({ firstName,lastname,phoneNumber,username, email, password: hashedPassword });
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  const passwordRegex = /^(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&#])[A-Za-z\d@$!%*?&#]{8,20}$/;
  const phoneRegex = /^\d{12}$/;

  if (!email || !emailRegex.test(email)) {
    return next(errorHandler(405,'Invalid email address'));
  }

  if (!password || !passwordRegex.test(password)) {
    return next(errorHandler(405,'Password must be 8-20 characters long, include at least one uppercase letter, one number, and one special character'));
  }

  if (!phoneNumber || !phoneRegex.test(phoneNumber)) {
    return next(errorHandler(405,'Phone number must be 12 digits'));
}

  try { 
    await newUser.save();
    res.status(201).json('User created successfully!');
  } catch (error) {
    next(error);
  }
};

export const signin = async (req, res, next) => {
  const { email, password } = req.body;
  try {
    const validUser = await User.findOne({ email });
    if (!validUser) return next(errorHandler(404, 'User not found!'));
    const validPassword = bcryptjs.compareSync(password, validUser.password);
    if (!validPassword) return next(errorHandler(401, 'Wrong credentials!'));
    const token = jwt.sign({ id: validUser._id }, process.env.JWT_SECRET);
    const { password: pass, ...rest } = validUser._doc;
    res
      .cookie('access_token', token, { httpOnly: true })
      .status(200)
      .json(rest);
  } catch (error) {
    next(error);
  }
};

